class Solution{
public:
    vector<ll> nthRowOfPascalTriangle(int n) {
        vector<ll> v(n, 1);
        for(int i = 1; i < n-1; i++){
            for(int j = i; j > 0; j--){
                v[j] += v[j-1];
                v[j] %= 1000000007;
            }
        }
        return v;
    }
};


//{ Driver Code Starts.


void printAns(vector<ll> &ans) {
    for (auto &x : ans) {
        cout << x << " ";
    }
    cout << "\n";
}

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        auto ans = ob.nthRowOfPascalTriangle(n);
        printAns(ans);
    }
    return 0;
}

